package com.offcn.po;

public class Grade {
    private Integer id;

    private Integer sid;

    private Integer cid;

    private Integer tid;

    private Double pgrade;

    private Double kgrade;

    private Double zgrade;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getSid() {
        return sid;
    }

    public void setSid(Integer sid) {
        this.sid = sid;
    }

    public Integer getCid() {
        return cid;
    }

    public void setCid(Integer cid) {
        this.cid = cid;
    }

    public Integer getTid() {
        return tid;
    }

    public void setTid(Integer tid) {
        this.tid = tid;
    }

    public Double getPgrade() {
        return pgrade;
    }

    public void setPgrade(Double pgrade) {
        this.pgrade = pgrade;
    }

    public Double getKgrade() {
        return kgrade;
    }

    public void setKgrade(Double kgrade) {
        this.kgrade = kgrade;
    }

    public Double getZgrade() {
        return zgrade;
    }

    public void setZgrade(Double zgrade) {
        this.zgrade = zgrade;
    }
}